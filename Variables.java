
public class Variables {

	public static void main(String[] args) {
		int a=100;
		char x='y';
		boolean y=false;
		short m=2000;
		float c=21.56f;
		byte n=10;
		long l=100000L;
		System.out.println(a);
		System.out.println(x);
		System.out.println(y);
		System.out.println(m);
		System.out.println(c);
		System.out.println(n);
		System.out.println(l);

	}

}
